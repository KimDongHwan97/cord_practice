// 제한 조건
// arr은 길이 1 이상인 배열입니다.
// 인덱스 i, j에 대해 i ≠ j이면 arr[i] ≠ arr[j] 입니다.




function solution(arr) {
    // 배열에서 가장 작은 값을 찾아 제거
    arr.splice(arr.indexOf(Math.min(...arr)), 1);

    // 배열의 길이가 0(비어 있다)인 경우 [-1] 반환
    if (arr.length < 1) return [-1];

    // 배열에 값이 남아 있다면 그대로 반환
    return arr;
}


